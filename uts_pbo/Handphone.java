package uts_pbo;

public class Handphone { //ini merupakan bagian main class
	private String Type;
    private String OS;
    private Integer RAM;

    public String getType() {
        return Type;
    }
    public void setType(String type) {
        Type = type;
    }
    public String getOS() {
        return OS;
    }
    public void setOS(String oS) {
        OS = oS;
    }
    public Integer getRAM() {
        return RAM;
    }
    public void setRAM(Integer rAM) {
        RAM = rAM;
    }
    
    
}